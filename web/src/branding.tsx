/** Returns a translator bound to the current global language. Unknown keys fall
 * back to the English string, then to the key itself, so a missing translation
 * degrades visibly-but-safely rather than crashing. Optional `params` fills
 * `{placeholder}` tokens in the resolved string (e.g. `t("x.y", { name })`). */
export function useT(): (key: string, params?: Record<string, string>) => string {
  const { branding } = useBranding();
  const lang = branding.language;
  return useCallback(
    (key: string, params?: Record<string, string>) => {
      const raw = TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
      if (!params) return raw;
      return Object.entries(params).reduce(
        (acc, [name, value]) => acc.replaceAll(`{${name}}`, value),
        raw,
      );
    },
    [lang],
  );
}
